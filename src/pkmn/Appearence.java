package pkmn;

public class Appearence {
        // Appearence class provides pkmn attributes for challenges
	private int beauty;			//pokemons beauty
	private int cool;			//pokemons coolness
	private int cute;			//pokemons cuteness
	private int smart;			//pokemons smartness
	private int tough;			//pokemons toughness
	private int sheen;			//pokemons sheen
	
	public Appearence() {
		this.beauty = 0;
		this.cool = 0;
		this.cute = 0;
		this.smart = 0;
		this.tough = 0;
		this.sheen = 0;
	}
}
